package modelo;

public class Envio {
	private int numEnvio;
	private String estado;
	
	public Envio(){
	}

	public int getNumEnvio() {
		return numEnvio;
	}

	public void setNumEnvio(int numEnvio) {
		this.numEnvio = numEnvio;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	};
	
	
}
